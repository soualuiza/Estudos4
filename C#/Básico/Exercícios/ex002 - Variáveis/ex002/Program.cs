using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace ex002
{
    internal class Program
    {
        static void Main(string[] args)
        {

            string nomeCompleto = "Luiza Machado"; // Variável do tipo String, nota-se que em C# não se começa uma variável com um número em seu título.
            int idade = 20;                        // Variável do tipo Int, nela trabalhamos com números inteiros. 
            double salario = 95.00;                // Variável do tipo double, nela trabalhamos com valores "quebrados", ou seja, números não exatos. 
            bool pessoaBonita = true;              // Variável do tipo oooleana, na qual se trata de valores verdadeiros ou falsos. 




            Console.WriteLine(nomeCompleto);
            Console.WriteLine(idade);
            Console.WriteLine(salario);
            Console.WriteLine(pessoaBonita);

        }
    }
}
